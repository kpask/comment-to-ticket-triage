package com.example.pulsedesk.enums;

/**
 * Enumeration of possible ticket priorities for support tickets.
 * Used to indicate the urgency and importance of resolving a ticket.
 */
public enum Priority {
    /** Critical issues requiring immediate attention and resolution */
    HIGH,
    /** Important issues that should be addressed soon but not urgently */
    MEDIUM,
    /** Minor issues that can be resolved when resources become available */
    LOW
}
